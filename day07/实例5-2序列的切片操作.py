s='HelloWorld'
#切片操作
s1=s[0:5:2]#索引从0开始，到5结束（不包含5）步长为2，
            # 相当于取字符串第1，3，5个字符
print(s1)
#省略开始位置，start从0开始
print(s[:5:1])#hello
print(s[0:5:2])#hlo
#省略开始位置start，省略步长step
print(s[0:5:])#hello,默认步长为1
print(s[0::1])#stop不写，默认到序列的最后一个元素（包含最后一个）
print(s[5::])#从5开始
print(s[5:])#和12行代码相同
#更改步长
print(s[0:5:2])
#省略开始位置，省略结束为止，只写步长
print(s[::2])#获取0,2,4,6,8，的元素，十个元素，0到9，没有10序号
#步长为负数
print(s[::-1])#把字符串进行逆序，颠倒操作
print(s[-1:-11:-1])